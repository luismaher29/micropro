import type { Level, QuestionStep, World } from "../game/types";

const createQuestion = (
  id: string,
  prompt: string,
  options: Array<[string, string]>,
  correctOptionId: string,
  explanation: string,
  theory?: string,
): QuestionStep => ({
  id,
  prompt,
  theory,
  correctOptionId,
  explanation,
  options: options.map(([optionId, label]) => ({ id: optionId, label })),
});

const createLevel = (
  id: string,
  title: string,
  description: string,
  difficulty: Level["difficulty"],
  xp: number,
  coins: number,
  steps: QuestionStep[],
): Level => ({
  id,
  title,
  description,
  difficulty,
  reward: { xp, coins },
  steps,
});

